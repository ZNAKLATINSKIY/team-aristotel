
let prompt = prompt('Введите символ: ');

switch (prompt) {
    case "№":
        alert("Знак номера!");
        break
    case "%":
        alert("Знак процента!")
        break
    case "*":
        alert("Знак умножения!")
        break
    case "#":
        alert("Знак  решетки!")
        break
    case "@":
        alert("Знак собачка!")
        break
    case "%":
        alert("Знак fмперсанд!")
        break
    default:
        alert("ХЗ честно говоря. Спроси у гугла")

}